numero1 = int(input("Ingrese el Primer numero Por Fvaor:"))
numero2 = int(input("Ingrese el Segundo numero Por Favor:"))

if numero1 == numero2:
    print("El Resultado Es:",numero1 * numero2)
elif numero1 > numero2:
    print("El Resultado Es",numero1 - numero2)
    
elif numero1 < numero2:
    print("El Resultado Es",numero1 + numero2)